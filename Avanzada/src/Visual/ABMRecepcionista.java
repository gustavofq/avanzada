    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int dni = Integer.parseInt(txtDNI.getText());
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        
        try {
            unaControladoraVisual.altaRecepcionista(dni, nombre, apellido);
        } catch (Exception ex) {
            Logger.getLogger(ABMRecepcionista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        cargarTabla();
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int dni = Integer.parseInt(txtDNI.getText());
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        
        Recepcionista unaRecepcionista = unaControladoraVisual.DameLaRecepcionista(dni);
        
        try {
            unaControladoraVisual.modificarRecepcionista(dni, nombre, apellido, unaRecepcionista);
        } catch (Exception ex) {
            Logger.getLogger(ABMRecepcionista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        cargarTabla();
        
    }//GEN-LAST:event_btnModificarActionPerformed

